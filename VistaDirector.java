    private void btnRegistrarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarEstudianteActionPerformed
        // 1. Leer campos
        String tipoDoc = comboTipoDoc.getSelectedItem().toString();
        String numDoc = txtNumDoc.getText().trim();
        String nombres = txtNombres.getText().trim();
        String apellidos = txtApellidos.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String nivel = comboNivelEducativo.getSelectedItem().toString();
        String tipoTurno = comboTurno.getSelectedItem().toString();
        String grado = comboGrado.getSelectedItem().toString();
        String seccion = comboSeccion.getSelectedItem().toString();
        String pass = new String(txtPassword.getPassword());
        String confirm = new String(txtConfirmarPassword.getPassword());
        Date fecha = jDateNacimiento.getDate();
        LocalDate fechaNac = fecha.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();

        // 2. Validaciones b√°sicas
        String error = ValidadorEstudiante.validar(
            tipoDoc, numDoc, nombres, apellidos,
            telefono, nivel, tipoTurno,
            grado, seccion, pass, confirm,fecha
        );

        if (error != null) {
            JOptionPane.showMessageDialog(this, error);
            return;
        }

        // 3. Verificar capacidad de secci√≥n
        EstudianteDAO estudianteDAO = new EstudianteDAO();
        
        MatriculaDAO matriculaDAO = new MatriculaDAO();
        
        int cantidad = matriculaDAO.contarEstudiantesEnSeccion(nivel, grado, seccion);
        
        if (cantidad >= 40) {
            JOptionPane.showMessageDialog(this, "La secci√≥n " + seccion + " ya tiene 40 estudiantes.");
            return;
        }

        // 4. Verificar que el documento no se repita
        if (estudianteDAO.existeDocumento(numDoc)) {
            JOptionPane.showMessageDialog(this, "Ya existe un estudiante con ese n√∫mero de documento.");
            return;
        }

        // 5. Crear estudiante
        Turno turno = new Turno(tipoTurno);
        Estudiante e = new Estudiante();
        e.setTipoDocIdentidad(new TipoIdentidad(tipoDoc));
        e.setCodigoDocIdentidad(numDoc);
        e.setNombres(nombres);
        e.setApellidos(apellidos);
        e.setNumCelular(telefono);
        e.setNivelEducativo(new NivelEducativo(nivel));
        e.setTurno(turno);
        e.setGrado(new Grado(grado));
        e.setSeccion(new Seccion(seccion));
        e.setFechaNacimiento(fechaNac);

        // ‚¨áÔ∏è NUEVO: Insertar estudiante y obtener su ID
        int idEstudiante = estudianteDAO.insertarYObtenerId(e);
        if (idEstudiante == -1) {
            JOptionPane.showMessageDialog(this, "Error al registrar estudiante.");
            return;
        }
        e.setId(idEstudiante); // Muy importante, para que Matricula reciba el ID

        // 6. Registrar la matr√≠cula
        Matricula m = new Matricula();
        m.setEstudiante(e);
        m.setNivelEducativo(new NivelEducativo(nivel));
        m.setGrado(new ModeloRegistro.Grado(grado));
        m.setFechaInicio(LocalDate.now());
        m.setEstado("Activo");
        Seccion seccionObj = new Seccion();
        seccionObj.setNombre(seccion);
        m.setSeccionn(seccionObj);
        matriculaDAO.insertar(m);

        // 7. Generar correo institucional y registrar usuario
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        String correoGenerado = GeneradorCorreo.generarCorreoInstitucional(nombres, apellidos, numDoc, usuarioDAO);
        Usuario u = new Usuario(correoGenerado, pass, "ESTUDIANTE");
        usuarioDAO.insertar(u);

        JOptionPane.showMessageDialog(this, "Estudiante registrado correctamente.\nCorreo: " + correoGenerado);
    }//GEN-LAST:event_btnRegistrarEstudianteActionPerformed

