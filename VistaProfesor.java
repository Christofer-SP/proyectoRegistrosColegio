    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        String gradoSeleccionado = (String) cbGrado.getSelectedItem();

        // Validación simple
        if (gradoSeleccionado == null || gradoSeleccionado.isEmpty()) {
           JOptionPane.showMessageDialog(this, "Seleccione un grado válido.");
           return;
        }

        // Obtener cursos del método personalizado
        List<String> cursos = asignarCursosPorGrado(gradoSeleccionado);

        // Obtener el modelo de la tabla y limpiarlo
        DefaultTableModel modelo = (DefaultTableModel) tbCalificaciones.getModel();
        modelo.setRowCount(0); // Limpia las filas existentes

        // Agregar los cursos como nuevas filas
        for (String curso : cursos) {
        modelo.addRow(new Object[]{curso, "", "", "", "", ""});
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private List<String> asignarCursosPorGrado(String grado) {
        List<String> cursos = new ArrayList<>();

        switch (grado) {
            case "1":
                cursos.add("Comunicación");
                cursos.add("Matemática");
                cursos.add("Ciencia y Tecnología");
                cursos.add("Ciencias Sociales");
                cursos.add("Desarrollo Personal, Ciudadanía y Cívica");
                cursos.add("Educación Física");
                cursos.add("Arte y Cultura");
                cursos.add("Educación Religiosa");
                cursos.add("Inglés");
                break;
            case "2":
            case "3":
                cursos.add("Comunicación");
                cursos.add("Matemática");
                cursos.add("Ciencia y Tecnología");
                cursos.add("Ciencias Sociales");
                cursos.add("Desarrollo Personal, Ciudadanía y Cívica");
                cursos.add("Educación Física");
                cursos.add("Arte y Cultura");
                cursos.add("Educación Religiosa");
                cursos.add("Inglés");
                cursos.add("Educación para el Trabajo");
            break;

            case "4":
            case "5":
                cursos.add("Comunicación");
                cursos.add("Matemática");
                cursos.add("Física");
                cursos.add("Química");
                cursos.add("Biología");
                cursos.add("Ciencias Sociales");
                cursos.add("Desarrollo Personal, Ciudadanía y Cívica");
                cursos.add("Educación para el Trabajo");
                cursos.add("Educación Física");
                cursos.add("Arte y Cultura");
                cursos.add("Educación Religiosa");
                cursos.add("Inglés");
                break;

            default:
                JOptionPane.showMessageDialog(this, "Grado no válido.");
                break;
        }
        return cursos;
}


    
