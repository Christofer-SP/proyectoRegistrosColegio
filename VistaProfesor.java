    private void cargarCursosIniciar(String grado) {
    List<String> cursos = asignarCursosPorGrado(grado);

    DefaultTableModel modelo = new DefaultTableModel(
        new Object[][]{}, 
        new String[]{
            "Curso", "Nombres", "Seccion", "Grado",
            "1er Periodo", "2do Periodo", "3er Periodo", "4to Periodo", "Promedio"
        }
    );

    for (String curso : cursos) {
        modelo.addRow(new Object[]{curso, "", "", grado, "", "", "", "", ""});
    }

    tbCalificaciones.setModel(modelo);
}


